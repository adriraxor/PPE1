    <h1>L'histoire du tennis de table</h1>



    <h3>Des origines aristocratiques :</h3>

    <p class="origines">Les historiens pensent que le tennis de table est n&eacute; au sein de la haute soci&eacute;t&eacute; de
        l'Angleterre victorienne, dans les ann&eacute;es 1880. Au d&eacute;part, il ne s'agit que d'une distraction, alternative au tennis sur gazon.
        &Agrave; l'&eacute;poque, le jeu est pratiqu&eacute; avec un &eacute;quipement approximatif : une rang&eacute;e de livres fait office de filet,
        le bout arrondi d'un bouchon de Champagne sert de balle tandis qu'un couvercle de cave &agrave; cigare est utilis&eacute; comme raquette !
    </p>



    <h3>&Eacute;volution :</h3>

    <p>En 1926, des comp&eacute;titions sont organis&eacute;es &agrave; Berlin et Londres, premiers pas vers la cr&eacute;ation de la Fédération
        de tennis de table. Les premiers Championnats du monde se d&eacute;roulent &agrave; Londres, en 1926. Mais le sport devra attendre 1988
        pour faire ses d&eacute;buts olympiques, &agrave; S&eacute;oul.</p>


    <h3>La technologie entre en jeu :</h3>

    <p>Le tennis de table a grandement progress&eacute; depuis son invention. Aujourd'hui, les joueurs utilisent des raquettes, construites &agrave;
        partir de mat&eacute;riaux sp&eacute;ciaux : bois enrob&eacute; de caoutchouc, fibre de carbone. La balle, ultra l&eacute;g&egrave;re,
        est d&eacute;sormais en cellulo&iuml;d creux. Ce mat&eacute;riel permet de r&eacute;aliser des smashes &agrave; plus de 150 km/h !</p>



    <h3>Attraction populaire :</h3>

    <p>On estime &agrave; 40 millions le nombre de joueurs licenci&eacute;s, auxquels il faut ajouter les nombreux millions de joueurs de loisirs.
        Ces chiffres font du tennis de table le sport le plus pratiqu&eacute; dans le monde. Ce succ&egrave;s est en grande partie le fait de son
        immense popularit&eacute; en Chine, nation dominante du tennis de table.</p>

